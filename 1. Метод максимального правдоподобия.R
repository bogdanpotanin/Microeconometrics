  # 3) старый добрый бутстрап
boot_iter <- 1000                                     # число итераций бутстрапа
boot_est = data.frame("mu" = rep(NA, boot_iter),      # каждую итерацию складываем оценки
                      "sigma" = rep(NA, boot_iter))   # в соответствующие строки датафрейма
for(i in 1:boot_iter)
{
  X_boot <- sample(x = X,                             # из выборки X формируем новую выборку
                   size = n,                          # такого же объема как X, используя выбор
                   replace = TRUE)                    # с возвращением (наблюдения могут повторяться)
  boot_est$mu[i] <- mean(X_boot)                      # для быстроты считаем оценки аналитически, но
  boot_est$sigma[i] <- sd(X_boot)  * (n - 1) / n      # можно было и численно через optim()
}
